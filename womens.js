  function displayData(data){
    data.forEach(function(elem){
      // console.log(elem)
      var box = document.createElement("div")
  
      var imgLink = document.createElement("a")
      imgLink.setAttribute("href", "index.html")
  
      var img = document.createElement("img")
      img.setAttribute("src", elem.image_url)
      img.setAttribute("class","productImg")
  
      var name = document.createElement("h3")
      name.innerText = elem.name
      name.setAttribute("class", "name")
  
      var priceBox = document.createElement("div")
      priceBox.setAttribute("class", "priceBox")
  
      var price = document.createElement("p")
      price.innerText ="Rs." + elem.price
      price.setAttribute("class", "price")
  
      var strikeOff = document.createElement("p")
      strikeOff.innerText = "Rs." + elem.strikedoffprice
      strikeOff.setAttribute("class", "strikeOff")
  
      var addCart = document.createElement("button")
      addCart.innerText = "Add To Cart"
      addCart.setAttribute("id", "addToCart")
      addCart.addEventListener("click", function(){
        addToCart(elem)
      })
  
      imgLink.append(img)
      priceBox.append(price,strikeOff)
  
      box.append(imgLink, name, priceBox, addCart)
      // console.log(box)
  
      document.querySelector("#container").append(box)
  })
  }

  function addToCart(cartData){
    let a = cartArr.filter(function(el){
      return el.name == cartData.name
    })

    if(a.length == 0){
      cartArr.push(cartData)
      alert("Item added to cart")
      localStorage.setItem("cartList", JSON.stringify(cartArr))
    }
    else{
      alert("Product already added")
    }
  }